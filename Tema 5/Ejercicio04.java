public class Ejercicio04 {
	
	public static void main (String[] args) {
		
    System.out.println("Este programa le mostrará los números del 320 al 160, contando de 20 en 20 hacia atrás.");
    
    for (int n = 320 ; n >= 160 ; n -= 20) {
      System.out.println(n);
    }  
	}
}


public class Ejercicio06 {
	
	public static void main (String[] args) {
		 
    System.out.println("Este programa le mostrará los números del 320 al 160, contando de 20 en 20 hacia atrás.");
    
    int n = 320;
    
    do {
      System.out.println(n);
      n -= 20;
    } while (n >= 160);
	}
}

